"""
结果汇总与对比

将多个 benchmark 的结果汇总为 DataFrame，生成比较图表和报告。
"""

from pathlib import Path
from typing import Dict, List

import pandas as pd
import matplotlib.pyplot as plt


def aggregate_results(benchmark_results: List[dict]) -> pd.DataFrame:
    """
    汇总多个 benchmark 的结果。

    Args:
        benchmark_results: 每个元素为 {"name": "memory_capacity", "results": {...}}。

    Returns:
        汇总 DataFrame。
    """
    rows = []
    for item in benchmark_results:
        name = item["name"]
        results = item["results"]
        # 取标量值
        scalar_vals = {k: v for k, v in results.items() if isinstance(v, (int, float))}
        rows.append({"benchmark": name, **scalar_vals})

    df = pd.DataFrame(rows)
    return df


def plot_comparison(
    results_dict: Dict[str, dict],
    output_dir: Path,
    title: str = "Memory Capacity Comparison",
) -> None:
    """
    绘制多次实验的 MC 曲线对比。

    Args:
        results_dict: {"实验A": mc_results, "实验B": mc_results, ...}
        output_dir: 保存目录。
        title: 图表标题。
    """
    plt.figure(figsize=(12, 7))
    colors = plt.cm.tab10.colors

    for idx, (exp_name, results) in enumerate(results_dict.items()):
        r2_curve = results.get("r2_curve", [])
        if not r2_curve:
            continue
        d_max = results.get("d_max", len(r2_curve))
        delays = range(1, d_max + 1)
        total_mc = results.get("total_mc", sum(r2_curve))
        color = colors[idx % len(colors)]
        plt.plot(delays, r2_curve, "o-", color=color, markersize=3,
                 label=f"{exp_name} (MC={total_mc:.2f})")

    plt.title(title, fontsize=14)
    plt.xlabel("Delay $d$", fontsize=12)
    plt.ylabel("Capacity ($R^2$)", fontsize=12)
    plt.ylim(0, 1.1)
    plt.grid(True, linestyle="--", alpha=0.6)
    plt.legend()
    output_dir.mkdir(parents=True, exist_ok=True)
    plt.savefig(output_dir / "mc_comparison.png", dpi=300)
    plt.close()
    print(f"对比图已保存: {output_dir / 'mc_comparison.png'}")


def generate_report(
    summary_df: pd.DataFrame,
    output_dir: Path,
    experiment_name: str = "benchmark_summary",
) -> None:
    """
    生成 Markdown 报告。

    Args:
        summary_df: 汇总 DataFrame。
        output_dir: 输出目录。
        experiment_name: 报告文件名前缀。
    """
    output_dir.mkdir(parents=True, exist_ok=True)

    # CSV
    csv_path = output_dir / f"{experiment_name}.csv"
    summary_df.to_csv(csv_path, index=False)

    # Markdown
    md_path = output_dir / f"{experiment_name}.md"
    with open(md_path, "w", encoding="utf-8") as f:
        f.write(f"# Swarm Reservoir Benchmark Report\n\n")
        f.write(f"## Summary\n\n")
        f.write(summary_df.to_markdown(index=False))
        f.write("\n\n---\n*Generated by SwarmReservoir v3.0*\n")

    print(f"报告已生成: {csv_path}, {md_path}")
